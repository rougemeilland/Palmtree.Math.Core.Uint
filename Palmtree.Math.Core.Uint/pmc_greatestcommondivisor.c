static void GreatestCommonDivisor_Imp(__UNIT_TYPE* p1_buf, __UNIT_TYPE* p2_buf, __UNIT_TYPE word_count, __UNIT_TYPE *w_buf, __UNIT_TYPE *w_buf_count)
{
    __UNIT_TYPE* u = p1_buf;
    __UNIT_TYPE* v = p2_buf;
    for (;;)
    {
        while (word_count > 0 && u[word_count - 1] == 0 && v[word_count - 1] == 0)
            --word_count;
        if (word_count <= 0)
        {
            // このルートへの到達はあり得ないはず。
            w_buf[0] = 0;
            *w_buf_count = 1;
        }
        int c = Compare_Imp(u, v, word_count);
        if (c == 0)
        {
            // u == v の場合

            // GCD を u として復帰
            _COPY_MEMORY_UNIT(w_buf, u, word_count);
            *w_buf_count = word_count;
            return;
        }
        if (c < 0)
        {
            __UNIT_TYPE* tt = u;
            u = v;
            v = tt;
        }
        // この時点で u > v かつ u と v はともに奇数である

        Subtruct_Imp(u, word_count, v, word_count, u, word_count);

        // この時点で u は偶数

        while ((*u & 1) == 0)
            RightShift_Imp(u, word_count, 1, u, TRUE);
    }
}

static PMC_STATUS_CODE PMC_GreatestCommonDivisor_X_I_Imp(NUMBER_HEADER* u, _UINT32_T v, NUMBER_HEADER** w)
{
    PMC_STATUS_CODE result;
    if (u->IS_ZERO)
    {
        // u が 0 である場合

        if (v == 0)
        {
            // v が 0 である場合

            // u と v がともに 0 であり、GCD は一意に存在しないため、エラーを返す。
            return (PMC_STATUS_ARGUMENT_ERROR);
        }
        else
        {
            // v が 0 ではない場合

            // GCD は v そのものであるため、v を計算結果として返す。
            if ((result = From_I_Imp(v, w)) != PMC_STATUS_OK)
                return (result);
        }
    }
    else
    {
        // u が 0 ではない場合

        if (v == 0)
        {
            // v が 0 である場合

            // GCD は u そのものであるため、u を計算結果として返す。
            if ((result = DuplicateNumber(u, w)) != PMC_STATUS_OK)
                return (result);
        }
        else
        {
            // u と v がともに 0 ではない場合

            // u と v の GCD を計算する
            __UNIT_TYPE u_bit_count = u->UNIT_BIT_COUNT;
            __UNIT_TYPE v_bit_count = sizeof(v) * 8 - _LZCNT_ALT_32(v);
            __UNIT_TYPE work_bit_count = _MAXIMUM_UNIT(u_bit_count, v_bit_count);

            __UNIT_TYPE work_u_buf_code;
            __UNIT_TYPE work_u_buf_words;
            __UNIT_TYPE* work_u_buf = AllocateBlock(work_bit_count, &work_u_buf_words, &work_u_buf_code);
            if (work_u_buf == NULL)
                return (PMC_STATUS_NOT_ENOUGH_MEMORY);

            __UNIT_TYPE work_v_buf_code;
            __UNIT_TYPE work_v_buf_words;
            __UNIT_TYPE* work_v_buf = AllocateBlock(work_bit_count, &work_v_buf_words, &work_v_buf_code);
            if (work_v_buf == NULL)
            {
                DeallocateBlock(work_u_buf, work_u_buf_words);
                return (PMC_STATUS_NOT_ENOUGH_MEMORY);
            }

            __UNIT_TYPE nw_check_code;
            if ((result = AllocateNumber(w, work_bit_count, &nw_check_code)) != PMC_STATUS_OK)
            {
                DeallocateBlock(work_v_buf, work_v_buf_words);
                DeallocateBlock(work_u_buf, work_u_buf_words);
                return (result);
            }

            __UNIT_TYPE u_tzcnt = u->TRAILING_ZERO_BITS_COUNT;
            __UNIT_TYPE v_tzcnt = _TZCNT_ALT_UNIT(v);
            __UNIT_TYPE k = _MINIMUM_UNIT(u_tzcnt, v_tzcnt);
            if (u_tzcnt > 0)
                RightShift_Imp(u->BLOCK, u->UNIT_WORD_COUNT, u_tzcnt, work_u_buf, FALSE);
            else
                _COPY_MEMORY_UNIT(work_u_buf, u->BLOCK, u->UNIT_WORD_COUNT);
            work_v_buf[0] = v >> v_tzcnt;

            __UNIT_TYPE w_buf_count;
            GreatestCommonDivisor_Imp(work_u_buf, work_v_buf, _DIVIDE_CEILING_UNIT(work_bit_count, __UNIT_TYPE_BIT_COUNT), (*w)->BLOCK, &w_buf_count);
            if ((result = CheckBlockLight(work_u_buf, work_u_buf_code)) != PMC_STATUS_OK)
                return (result);
            if ((result = CheckBlockLight(work_v_buf, work_v_buf_code)) != PMC_STATUS_OK)
                return (result);
            if ((result = CheckBlockLight((*w)->BLOCK, nw_check_code)) != PMC_STATUS_OK)
                return (result);
            DeallocateBlock(work_v_buf, work_v_buf_words);
            DeallocateBlock(work_u_buf, work_u_buf_words);

            LeftShift_Imp((*w)->BLOCK, w_buf_count, k, (*w)->BLOCK, TRUE);
            if ((result = CheckBlockLight((*w)->BLOCK, nw_check_code)) != PMC_STATUS_OK)
                return (result);
            CommitNumber(*w);
        }
    }
    return (PMC_STATUS_OK);
}

